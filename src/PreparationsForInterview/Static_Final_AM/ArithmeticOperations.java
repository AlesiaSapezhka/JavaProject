package PreparationsForInterview.Static_Final_AM;

public final class ArithmeticOperations {
    //  Разработай класс с полезными методами (например, арифметические операции), от которого нельзя унаследоваться.
    // Пояснение: определи, как запретить наследование, и продумай, какие методы можно сделать статическими.

    public static int sum(int a, int b){
        return a+b;
    }

    public static int subtraction(int a, int b){
        return a-b;
    }
}
